#include <iostream>

// introducing namespace. you can tell it to use std as namespace to save a lot of time
using namespace std;

int main(){
    // As you can see here, we are not putting std:: before string, and before cout... etc
    // and we are also not writing std::endl; to end and print the Hello
    string greeting = "Hello\n";
    cout << greeting;
    return 0;
    // This can save you a lot of time programming / Coding

    
}